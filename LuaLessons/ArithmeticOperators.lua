--[[
    In Lua, arithmetic operators are used to perform mathematical operations on numeric values. The arithmetic operators in Lua include:

    Addition (+) - adds two values together
    Subtraction (-) - subtracts one value from another
    Multiplication (*) - multiplies two values together
    Division (/) - divides one value by another
    Modulo (%) - returns the remainder of a division operation
    Exponentiation (^) - raises a value to a power    
--]]


-- Here are some examples of how arithmetic operators can be used in Lua:
-- addition
local a = 5
local b = 10
local result = a + b -- result is 15

-- subtraction
local c = 8
local d = 3
local result = c - d -- result is 5

-- multiplication
local e = 4
local f = 7
local result = e * f -- result is 28

-- division
local g = 10
local h = 2
local result = g / h -- result is 5

-- modulo
local i = 17
local j = 3
local result = i % j -- result is 2

-- exponentiation
local k = 2
local l = 4
local result = k ^ l -- result is 16

-- Note that arithmetic operators only work with numeric values. If you try to use an arithmetic operator with a non-numeric value, Lua will throw an error.