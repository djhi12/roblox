--[[
    In Lua, comments are used to add annotations and explanations to your code without affecting its functionality. Lua supports two types of comments:    
--]]


-- Single-line comments: These comments start with two hyphens (--), followed by the comment text. Anything written after the two hyphens until the end of the line is considered a comment.

-- This is a single-line comment in Lua


-- Multi-line comments: These comments start with --[[ and end with --]]. Anything written between the start and end of the comment is considered a comment, even if it spans multiple lines.

--[[
This is a multi-line comment in Lua
It can span multiple lines
--]]



--[[
    Comments are often used to explain the purpose of functions or variables, to leave notes to yourself or other developers, or to temporarily disable a block of code during testing or debugging.    
--]]

