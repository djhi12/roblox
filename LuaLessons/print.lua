-- In Lua, the print function is used to output text to the console or terminal. It is a built-in function that takes one or more arguments and prints their values to the console.

-- For example, to print the string "Hello, world!" to the console, you would use the following code:

print("Hello, world!")


-- This would output the following to the console:

-- Hello, world!

-- You can also print the values of variables by passing them as arguments to the print function. For example:

local x = 10
local y = 20
print(x, y)



