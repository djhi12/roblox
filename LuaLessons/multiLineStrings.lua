--[[
    In Lua, [[ and ]] are used to delimit long strings, which can span multiple lines and include any characters without needing to escape them. Long strings are commonly used for embedding chunks of code, documentation, or data in Lua scripts.    
--]]

-- Here is an example of a long string in Lua using [[ and ]]:
long_string = [[
This is a long string
that spans multiple lines.
It can include any characters,
including "quotes" and [brackets].
]]

-- In this example, the [[ and ]] delimiters are used to enclose the long string, which includes multiple lines of text, as well as special characters such as double quotes and square brackets, without needing to escape them.