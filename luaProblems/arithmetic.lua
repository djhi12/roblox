local num1 = 10
local num2 = 5

local sum = num1 + num2
local difference = num1 - num2
local product = num1 * num2
local quotient = num1 / num2
local modulo = num1 % num2

print("Sum:", sum)
print("Difference:", difference)
print("Product:", product)
print("Quotient:", quotient)
print("Modulo:", modulo)

-- In this program, we declare two local variables num1 and num2 and assign them the values 10 and 5, respectively. We then use arithmetic operators to perform basic math operations on these variables and store the results in new local variables sum, difference, product, quotient, and modulo. Finally, we print out these results using the print() function.


--[[
    Output:
    Sum:      15
    Difference: 5
    Product: 50
    Quotient: 2
    Modulo:   0
--]]

-- This is because sum is the result of adding num1 and num2, which is 15. difference is the result of subtracting num2 from num1, which is 5. product is the result of multiplying num1 and num2, which is 50. quotient is the result of dividing num1 by num2, which is 2. modulo is the remainder when num1 is divided by num2, which is 0 in this case because num1 is evenly divisible by num2.